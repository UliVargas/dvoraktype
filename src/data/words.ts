export function getRandomWords(language: "es" | "en", count: number): string {
  const words = language === "es" ? commonWordsEs : commonWordsEn;
  let result = "";
  for (let i = 0; i < count; i++) {
    const randomWord = words[Math.floor(Math.random() * words.length)];
    result += randomWord + (i === count - 1 ? "" : " ");
  }
  return result;
}
